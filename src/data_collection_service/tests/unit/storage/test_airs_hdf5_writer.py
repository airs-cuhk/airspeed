def test_root_attrs():
    d = tempfile.mkdtemp()
    try:
        w = AirsHdf5Writer(d, description="d", robot_type="r", series_number="s")
        w.open_episode("ep")
        w.register_vector_stream("v", 1, columns=("x",))
        w.append_vector("v", [1.0], 1_700_000_000_000_000_000)
        path = w.close_episode(sample_rate=10.0)

        with h5py.File(path, "r") as f:
            assert f.attrs["description"] == "d"
            assert f.attrs["robot_type"] == "r"
            assert f.attrs["series_number"] == "s"
            assert f.attrs["sample_rate"] == 10.0
            assert f.attrs["frames"] == 1
    finally:
        import shutil; shutil.rmtree(d)

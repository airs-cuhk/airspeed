import h5py, numpy as np, tempfile, os
from core.storage import AirsHdf5Writer


def test_writes_vector_group():
    d = tempfile.mkdtemp()
    try:
        w = AirsHdf5Writer(d, description="test", robot_type="bot", series_number="S1")
        w.open_episode("ep001")
        w.register_vector_stream("joints", 3, columns=("j0", "j1", "j2"))
        for i in range(5):
            w.append_vector("joints", [float(i), i * 0.1, i * 0.2], int(1e9 * i / 30 + 1.7e18))
        path = w.close_episode(sample_rate=30.0)

        with h5py.File(path, "r") as f:
            assert "joints" in f
            g = f["joints"]
            assert g.attrs["type"] == "vector"
            assert g["data"].shape == (5, 3)
            assert g["data"].dtype == np.float32
            assert g["timestamps"].shape == (5,)
            assert g["timestamps"].dtype == np.uint64
            assert "columns" in g.attrs
    finally:
        import shutil; shutil.rmtree(d)


